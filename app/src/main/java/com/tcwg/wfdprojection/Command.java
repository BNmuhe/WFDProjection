package com.tcwg.wfdprojection;

public class Command {

    private String type;
    private Float x;
    private Float y;


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Float getX() {
        return x;
    }

    public void setX(Float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(Float y) {
        this.y = y;
    }
}
